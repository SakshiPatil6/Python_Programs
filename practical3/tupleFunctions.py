tuple1=tuple(input("Enter tuple: "))
print("Tuple1: ",tuple1)

#display length of tuple
print("Length of tuple1: ",len(tuple1))

#display min of tuple
print("Min element of tuple1: ",min(tuple1))

#display max of tuple
print("Max element of tuple1: ",max(tuple1))

#display sort of tuple
print("Sorted tuple1: ",sorted(tuple1))

#display sum of tuple
tuple2=[1,2,3,4]
print('Tuple2: ',tuple2)
print("Sum of tuple2: ",sum(tuple2))

#display type of tuple
print("Type of tuple: ",type(tuple1))

#reverse tuple with slicing
print("Displaying Tuple1 in reverse(Slicing): ",tuple1[::-1])
